public class AlphaAndOmega {

    // char data type can be set - not only with character enclosed with single quotes (eg. 'k', 'r', 'b')
    // - but also by providing integer value of character ASCII code

    public static void main(String[] args) {

        char alpha = 65;        // weird but valid
        char omega = 79;        // weird but valid

        System.out.println(alpha);
        System.out.println(omega);
    }
}
